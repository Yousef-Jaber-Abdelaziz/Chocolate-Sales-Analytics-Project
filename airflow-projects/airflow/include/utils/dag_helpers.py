def transfer_duckdb_to_postgres():
    duckdb_path = '/usr/local/airflow/include/STG/chocolate_warehouse.duckdb'
    
    connection_url = URL.create(
        drivername="postgresql+psycopg2",
        username="dwh_user",
        password="dwh_password",  
        host="host.docker.internal",
        port=5434,                # <--- The new port!
        database="dwh"
    )

    target_tables = [
        'stg_fact_sales', 
        'stg_dim_customers', 
        'stg_dim_stores', 
        'stg_dim_products',
        'stg_dim_locations'
    ]
    
    # ==========================================
    # ✅ 1. NEW: Initialize the grand total counter
    # ==========================================
    total_rows_transferred = 0  
    
    try:
        # ✅ Test connection first before touching DuckDB
        pg_engine = create_engine(connection_url)
        with pg_engine.connect() as test_conn:
            print("✅ PostgreSQL connection successful!")

        duck_conn = duckdb.connect(duckdb_path, read_only=True)  # ← read only, safer
        
        for table in target_tables:
            print(f"📖 Reading {table} from DuckDB...")
            df = duck_conn.execute(f"SELECT * FROM {table}").df()
            
            row_count = len(df)  # Store the length so we can use it multiple times
            print(f"   → {row_count} rows found")
            
            df.to_sql(
                table,
                pg_engine,
                if_exists='replace',
                index=False,
                schema='public'
            )
            print(f"✅ {table} → PostgreSQL ({row_count} rows)")
            
            # ==========================================
            # ✅ 2. NEW: Add this table's rows to the total
            # ==========================================
            total_rows_transferred += row_count  
            
        # ==========================================
        # ✅ 3. NEW: Return the total so Airflow catches it
        # ==========================================
        return total_rows_transferred  
            
    except Exception as e:
        print(f"❌ Transfer failed: {e}")
        raise                   # ← fail the task properly
    finally:
        # The finally block will still safely close connections 
        # BEFORE the return value is actually sent back to Airflow!
        if 'duck_conn' in locals():
            duck_conn.close()
        if 'pg_engine' in locals():
            pg_engine.dispose()
        print("🏁 Transfer process finished.")




