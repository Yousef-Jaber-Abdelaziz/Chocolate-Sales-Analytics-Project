def truncate_duckdb_staging(duckdb_path):
    """Connects to DuckDB and truncates all base tables except raw_locations."""
    print(f"Connecting to DuckDB at: {duckdb_path}")
    import duckdb # Ensure duckdb is imported if not globally available
    conn = duckdb.connect(duckdb_path)
    
    # Dynamically find all tables in the database
    tables = conn.execute("SELECT table_name FROM information_schema.tables WHERE table_type='BASE TABLE'").fetchall()
    
    for table in tables:
        table_name = table[0]
        
        # Check if the table is the one we want to protect
        if table_name == 'raw_locations':
            print(f"⏭️  Skipping truncation for protected table: {table_name}")
            continue # This jumps to the next table in the loop
            
        print(f"🧹 Truncating table: {table_name}")
        conn.execute(f"TRUNCATE TABLE {table_name}")
        
    print("All target DuckDB staging tables truncated successfully.")
    conn.close()